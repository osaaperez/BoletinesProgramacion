package com.programacion.Boletin9;


public class CalcDousNum {
    
    public void multiplicar(){
        
        double resposta = 1;
        
        for(int i=10;i<=90;i++){
        resposta = resposta*i;
    }
        System.out.println(resposta);   
        
    }
    public void sumar(){
        int resposta = 0;
        
        for (int i=10;i<=90;i++){
            resposta = resposta + i;
        }
        System.out.println(resposta);
    }   
        
        
        
    
    
    
    
    
    
    
    
    
    
    
}
